#include <stdio.h>
#include <string.h>
const char* digits[10][7] = {
    {"*****", "*   *", "*   *", "*   *", "*   *", "*   *", "*****"},  
    {"    *", "    *", "    *", "    *", "    *", "    *", "    *"}, 
    {"*****", "    *", "    *", "*****", "*    ", "*    ", "*****"},  
    {"*****", "    *", "    *", "*****", "    *", "    *", "*****"},  
    {"*   *", "*   *", "*   *", "*****", "    *", "    *", "    *"},  
    {"*****", "*    ", "*    ", "*****", "    *", "    *", "*****"},  
    {"*****", "*    ", "*    ", "*****", "*   *", "*   *", "*****"},  
    {"*****", "    *", "    *", "    *", "    *", "    *", "    *"},  
    {"*****", "*   *", "*   *", "*****", "*   *", "*   *", "*****"}, 
    {"*****", "*   *", "*   *", "*****", "    *", "    *", "*****"}   
};

int main() {
    int k;  
    scanf("%d", &k);
    while (k--) {
        int x;  
        scanf("%d", &x);
        printf("%d:\n", x);
        char str[10];
        sprintf(str, "%d", x);
        for (int i = 0; i < 7; i++) {  
            for (int j = 0; str[j] != '\0'; j++) { 
                int digit = str[j] - '0';  
                printf("%s", digits[digit][i]); 
                if (str[j + 1] != '\0') {
                    printf("  "); 
                }
            }
            printf("\n");  
        }
    }
    return 0;
}

